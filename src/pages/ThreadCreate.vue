<template>
  <div class="col-full push-top">
    <h1>
      Create new thread in <i>{{ forum.name }}</i>
    </h1>

    <form @submit.prevent="save">
      <div class="form-group">
        <label for="thread_title">Title:</label>
        <input v-model="title" type="text" id="thread_title" class="form-input" name="title" />
      </div>

      <div class="form-group">
        <label for="thread_content">Content:</label>
        <textarea v-model="text" id="thread_content" class="form-input" name="content" rows="8" cols="140"></textarea>
      </div>

      <div class="btn-group">
        <button class="btn btn-ghost" @click="cancel">Cancel</button>
        <button class="btn btn-blue" type="submit" name="Publish">
          Publish
        </button>
      </div>
    </form>
  </div>
</template>
<script setup lang="ts">
import { ref } from "vue"
import { useRouter, useRoute } from "vue-router";
import { useForumStore } from "@/stores/ForumStore"
import { useThreadsStore } from "@/stores/ThreadsStore";
const router = useRouter();
const route = useRoute()
const { createThread } = useThreadsStore();

const { findForumById } = useForumStore();

const props = defineProps({
  forumId: {
    type: String,
    required: true
  }
})
const forum = findForumById(props.forumId);
const title = ref('')
const text = ref('')
const cancel = () => {
  router.push({
    name: 'Forum',
    params: {
      id: props.forumId
    }
  })
}
const save = async () => {
  const thread = await createThread(
    {
      title: title.value,
      text: text.value,
      forumId: props.forumId
    }
  )

  router.push({
    name: 'ThreadShow',
    params: {
      threadId: thread?.id
    }
  })




  // dispatch a vuex action
}

</script>